<h2>Q&A</h2>

<details>
  <summary>I see a game I gave you on the list! What the hell?!</summary>
  <p>
    It's probably a duplicate. I am so fortunate to have people in my life who know that unwrapping
    a new board game is a joy above all others, and as I don't keep a public list of games
    somewhere, duplicates were inevitable. But fear not! I'm only giving away <em>someone else's</em
    > copy, not yours, the one that I love and treasure.
  </p>
  <p>
    I've also had the pleasure to play many of these extensively at a different time in my life, and
    I'm hoping they can bring other gamers as much joy as they've brought me over the years.
  </p>
</details>

<details>
  <summary>Why are you downsizing now?</summary>
  <p>
    Do you see how many games are on this list? The list of games I'm giving away? This isn't half
    of the games here. I live in a warehouse. And if I don't do it soon, I'm going to be living in a
    warehouse <em>alone</em>.
  </p>
</details>

<details>
  <summary>Why do you have so many unopened games?</summary>
  <p>
    That's a rude thing to ask an American; everyone knows the important thing is Having More
    Stuff&trade;, rather than enjoying what you already have.
  </p>
  <p>
    Just kidding, the issue was mostly optimism. A genuine hope that adults with busy lives would
    have endless time to try new games, and a willing imagination that fed me image after image of a
    happy brunch & board games for each shrink-wrapped box.
  </p>
  <p>
    There may also have been one or two purchases appended to reach free shipping thresholds. Hey, I
    already admitted I was American.
  </p>
</details>

<details>
  <summary>Are the opened games in good condition?</summary>
  <p>
    I am pretty protective about my games, but the ones that have been played more may show a little
    wear and tear. Nothing that would make the game unplayable or unsightly. Plus, it's hard to beat
    the low price of free.
  </p>
</details>

<details>
  <summary>I don't live in Austin. Will you ship it to me?</summary>
  <p>If you don't mind Venmoing me the shipping, I'll gladly put it in a box with an address.</p>
</details>

